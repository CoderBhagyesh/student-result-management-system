    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
                String rollNo = roll.getText();
        try {
            // register
            Class.forName("com.mysql.cj.jdbc.Driver");
            // create connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Richest@1");  
            // create statement
            Statement st = con.createStatement();
            // store the result fetched from sql in ResultSet
            ResultSet rs = st.executeQuery("select * from student inner join result where student.roll_no="+rollNo+" and result.roll_no="+rollNo+"");
            
            if(rs.next()) {
                course.setText(rs.getString(2));
                branch.setText(rs.getString(3));
                fullname.setText(rs.getString(4));
                gender.setText(rs.getString(5));
                javas.setText(rs.getString(7));
                os.setText(rs.getString(8));
                dbms.setText(rs.getString(9));
                cn.setText(rs.getString(10));
                
                // to calculate total marks convert the marks which are in string type into the int type
                int s1 = Integer.parseInt(javas.getText());
                int s2 = Integer.parseInt(os.getText());
                int s3 = Integer.parseInt(dbms.getText());
                int s4 = Integer.parseInt(cn.getText());
                
                String totalMarks = String.valueOf(s1+s2+s3+s4);
                
                total.setText(totalMarks);
            }
         
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

