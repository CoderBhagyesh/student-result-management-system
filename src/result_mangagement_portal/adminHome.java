    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String)courseName.getSelectedItem();
        String branch = (String) branchName.getSelectedItem();
        String rollNo = roll.getText();
        String fullName = name.getText();
        String genderv = (String)gender.getSelectedItem();
        try {
            // register
            Class.forName("com.mysql.cj.jdbc.Driver");
            // create connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Richest@1");  
            // create statement
            Statement st = con.createStatement();
            st.executeUpdate("insert into student values("+rollNo+", '"+course+"', '"+branch+"', '"+fullName+"', '"+genderv+"')");
            JOptionPane.showMessageDialog(null, "Data succesfully saved!");
            this.setVisible(false);
            new adminHome().setVisible(true);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_jButton6ActionPerformed

